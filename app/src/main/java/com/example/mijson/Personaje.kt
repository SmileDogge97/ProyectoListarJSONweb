package com.example.mijson

class Personaje(nombre:String, edad:String, genero:String, email:String, localidad:String, telefono:String) {

    var nombre = ""
    var edad = ""
    var genero = ""
    var email = ""
    var localidad = ""
    var telefono = ""

    init {
        this.nombre = nombre
        this.edad = edad
        this.genero = genero
        this.email = email
        this.localidad = localidad
        this.telefono = telefono
    }

}